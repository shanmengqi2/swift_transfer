export type TranslationKey = keyof (typeof translations)["en"];

export function isLanguage(value: string | null | undefined): value is Language {
  return languages.includes(value as Language);
}

export function isChineseLanguage(value: string | null | undefined) {
  return value?.toLocaleLowerCase().startsWith("zh") ?? false;
}

export function detectPreferredLanguage(languageValues: readonly string[]) {
  return languageValues.some(isChineseLanguage) ? "zh" : "en";
}

export function translate(
  language: Language,
  key: TranslationKey,
  params?: Record<string, string | number>,
) {
  let value: string = translations[language][key];

  if (!params) {
    return value;
  }

  for (const [paramKey, paramValue] of Object.entries(params)) {
    value = value.replaceAll(`{${paramKey}}`, String(paramValue));
  }

  return value;
}

export function formatDateTime(
  value: string | null,
  language: Language,
  fallbackKey: TranslationKey = "common.never",
) {
  if (!value) {
    return translate(language, fallbackKey);
  }

  return new Intl.DateTimeFormat(language === "zh" ? "zh-CN" : "en-US", {
    dateStyle: "medium",
    timeStyle: "short",
  }).format(new Date(value));
}

export function countLabel(
  language: Language,
  count: number,
  enSingular: string,
  enPlural: string,
  zhUnit: string,
) {
  return language === "zh"
    ? `${count} ${zhUnit}`
    : `${count} ${count === 1 ? enSingular : enPlural}`;
}
